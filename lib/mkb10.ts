export function searchMkb(query: string, limit = 50): MkbRow[] {
  const q = query.trim().toLowerCase();
  if (!q) return MKB_DATA.slice(0, limit);
  const out: MkbRow[] = [];
  for (const row of MKB_DATA) {
    if (row[0].toLowerCase().includes(q) || row[1].toLowerCase().includes(q)) {
      out.push(row);
      if (out.length >= limit) break;
    }
  }
  return out;
}

export function rowsInChapter(chapterIdx: number): MkbRow[] {
  return MKB_DATA.filter((r) => r[2] === chapterIdx);
}

export function chapterCounts(): number[] {
  const counts = new Array(MKB_CHAPTERS.length).fill(0);
  for (const r of MKB_DATA) counts[r[2]]++;
  return counts;
}
